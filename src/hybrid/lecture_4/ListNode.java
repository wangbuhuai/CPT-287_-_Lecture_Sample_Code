package hybrid.lecture_4;

/** A singly-linked list of integers */
public class ListNode {
    // Data fields
    public int val;  // Integer value stored in the node
    public ListNode next;  // Reference to the next node

    // Constructor
    public ListNode(int x) { val = x; }
}